/**
 * Copy Tesseract worker/core and PDF.js worker into public/ for self-hosted OCR.
 * Language data (eng.traineddata.gz) is downloaded once if missing.
 */
import { copyFileSync, existsSync, mkdirSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const ocrDir = join(root, "public", "ocr");
const pdfDir = join(root, "public", "pdfjs");

mkdirSync(ocrDir, { recursive: true });
mkdirSync(pdfDir, { recursive: true });

function copy(src, dest) {
  copyFileSync(src, dest);
  console.log(`copied ${dest.replace(root, ".")}`);
}

const workerSrc = join(root, "node_modules", "tesseract.js", "dist", "worker.min.js");
copy(workerSrc, join(ocrDir, "worker.min.js"));

const coreDir = join(root, "node_modules", "tesseract.js-core");
for (const name of readdirSync(coreDir)) {
  if (
    name.startsWith("tesseract-core") &&
    (name.endsWith(".js") || name.endsWith(".wasm"))
  ) {
    copy(join(coreDir, name), join(ocrDir, name));
  }
}

const pdfWorker = join(root, "node_modules", "pdfjs-dist", "build", "pdf.worker.min.mjs");
copy(pdfWorker, join(pdfDir, "pdf.worker.min.mjs"));

const langFile = join(ocrDir, "eng.traineddata.gz");
const langUrl =
  "https://cdn.jsdelivr.net/npm/@tesseract.js-data/eng@1.0.0/4.0.0/eng.traineddata.gz";

if (!existsSync(langFile)) {
  console.log("Downloading eng.traineddata.gz…");
  const res = await fetch(langUrl);
  if (!res.ok) {
    throw new Error(`Failed to download eng.traineddata.gz: ${res.status}`);
  }
  const buf = Buffer.from(await res.arrayBuffer());
  writeFileSync(langFile, buf);
  console.log(`wrote ./public/ocr/eng.traineddata.gz (${buf.length} bytes)`);
} else {
  console.log("eng.traineddata.gz already present");
}

writeFileSync(
  join(ocrDir, "README.md"),
  [
    "# Self-hosted OCR assets",
    "",
    "Generated by `npm run sync:ocr-assets`.",
    "Do not load these from an arbitrary CDN at runtime.",
    "",
  ].join("\n"),
);

console.log("OCR asset sync complete.");
