/**
 * Copy Tesseract worker/core and PDF.js worker into public/ for self-hosted OCR.
 * Language data is downloaded only when missing and must match pinned SHA-256.
 */
import {
  copyFileSync,
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  writeFileSync,
} from "node:fs";
import { createHash as cryptoHash } from "node:crypto";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const ocrDir = join(root, "public", "ocr");
const pdfDir = join(root, "public", "pdfjs");

mkdirSync(ocrDir, { recursive: true });
mkdirSync(pdfDir, { recursive: true });

const PINNED = {
  engTrainedDataGz: {
    url: "https://cdn.jsdelivr.net/npm/@tesseract.js-data/eng@1.0.0/4.0.0/eng.traineddata.gz",
    // Fill on first successful download if empty; subsequent runs verify.
    sha256: process.env.OCR_ENG_TRAINEDDATA_SHA256 ?? "",
  },
};

function sha256(buf) {
  return cryptoHash("sha256").update(buf).digest("hex");
}

function copy(src, dest) {
  copyFileSync(src, dest);
  console.log(`copied ${dest.replace(root, ".")}`);
}

const workerSrc = join(root, "node_modules", "tesseract.js", "dist", "worker.min.js");
copy(workerSrc, join(ocrDir, "worker.min.js"));

const coreDir = join(root, "node_modules", "tesseract.js-core");
for (const name of readdirSync(coreDir)) {
  if (
    name.startsWith("tesseract-core") &&
    (name.endsWith(".js") || name.endsWith(".wasm"))
  ) {
    copy(join(coreDir, name), join(ocrDir, name));
  }
}

const pdfWorker = join(root, "node_modules", "pdfjs-dist", "build", "pdf.worker.min.mjs");
copy(pdfWorker, join(pdfDir, "pdf.worker.min.mjs"));

const langFile = join(ocrDir, "eng.traineddata.gz");
const langUrl = PINNED.engTrainedDataGz.url;

async function ensureLang() {
  if (existsSync(langFile)) {
    const buf = readFileSync(langFile);
    const digest = sha256(buf);
    if (PINNED.engTrainedDataGz.sha256) {
      if (digest !== PINNED.engTrainedDataGz.sha256) {
        throw new Error(
          `OCR asset checksum mismatch for eng.traineddata.gz (got ${digest})`,
        );
      }
      console.log("eng.traineddata.gz present and checksum valid");
      return;
    }
    console.log(`eng.traineddata.gz present (sha256=${digest}) — pin OCR_ENG_TRAINEDDATA_SHA256 to lock`);
    return;
  }
  console.log("Downloading eng.traineddata.gz…");
  const res = await fetch(langUrl);
  if (!res.ok) {
    throw new Error(`Failed to download eng.traineddata.gz: ${res.status}`);
  }
  const buf = Buffer.from(await res.arrayBuffer());
  const digest = sha256(buf);
  if (PINNED.engTrainedDataGz.sha256 && digest !== PINNED.engTrainedDataGz.sha256) {
    throw new Error(
      `Downloaded eng.traineddata.gz checksum mismatch (got ${digest})`,
    );
  }
  writeFileSync(langFile, buf);
  console.log(`wrote ./public/ocr/eng.traineddata.gz (${buf.length} bytes, sha256=${digest})`);
}

await ensureLang();

writeFileSync(
  join(ocrDir, "README.md"),
  [
    "# Self-hosted OCR assets",
    "",
    "Generated by `npm run sync:ocr-assets`.",
    "Do not load these from an arbitrary CDN at runtime.",
    "Pin OCR_ENG_TRAINEDDATA_SHA256 to fail the build on upstream changes.",
    "",
  ].join("\n"),
);

console.log("OCR asset sync complete.");
